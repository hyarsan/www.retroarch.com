        <h2>The lobby</h2>
        <div class="row imagegrid">
            <div class="col-sm-6">
                <p>The most convenient way to try netplay is to use the <b>lobby</b> in the menu.</p>
                <p>You need an active internet connection and a router that supports UPnP port forwarding.</p>
                <p>You will also need to own <b>the same ROM</b> as your partner and have it <b>scanned</b> and in your playlists. We recommand ROMs from the No-Intro sets.</p>
                <p>To join a netplay room in the lobby, just refresh the room list and press A on a room entry.</p>
                <p>To host your own netplay session, press A on <b>Start netplay host</b> and a room will be created an announced publicly.</p>
                <p>This lobby will also work for local rooms (on your home local network).</p>
            </div>
            <div class="col-sm-6">
