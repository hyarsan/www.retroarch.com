        <h2>Presentation</h2>
        <p>RetroArch enables you to play over the network to games that were supporting two or more players. It means that you can challenge players around the world on classic games like Mario Kart, Bomberman, Street Fighter, or team up on games like Streets of Rage or Sonic 3.</p>
        <p>Participants will be connected in peer to peer to reduce network latency and ensure the best possible experience.</p>

