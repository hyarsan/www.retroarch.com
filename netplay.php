        <h2>Manual netplay</h2>
        <div class="row imagegrid">
            <div class="col-sm-6">
                <img src="images/netplay-settings.png">
            </div>
            <div class="col-sm-6">
                <p>Advanced users have the possibility to setup netplay manually.</p>
                <p>The <b>Network Settings</b> enables you to tweak how the network code will behave when you are acting as <b>Server</b>.</p>
                <p>If you don't need <b>NAT tarversal</b>, it can be disabled in the settings.</p>
                <p>When you are acting as client, most of these settings will have no effect. The only things you will have to set will be the <b>Server IP</b>.</p>
